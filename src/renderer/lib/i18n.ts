export function formatRowCount(locale: Locale, shown: number, total: number): string {
  if (locale === "zh") return `共 ${total} 行,当前显示 ${shown} 行`;
  return `${shown} of ${total} rows`;
}

const TRANSLATIONS: Record<Locale, Record<TranslationKey, string>> = { en: EN, zh: ZH };

export interface I18nContextValue {
  locale: Locale;
  t: (key: TranslationKey) => string;
  setLocale: (locale: Locale) => void;
}

const I18nContext = createContext<I18nContextValue>({
  locale: "en",
  t: (key) => EN[key] ?? key,
  setLocale: () => {}
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    if (typeof window === "undefined") return "en";
    const stored = window.localStorage.getItem(STORAGE_KEY);
    return stored === "zh" ? "zh" : "en";
  });

  useEffect(() => {
    if (typeof window === "undefined") return;
    window.localStorage.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale === "zh" ? "zh-Hans" : "en";
  }, [locale]);

  const value: I18nContextValue = {
    locale,
    setLocale: setLocaleState,
    t: (key) => TRANSLATIONS[locale][key] ?? EN[key] ?? key
  };

  return createElement(I18nValueProvider, { value }, children);
}

export function I18nValueProvider({
  value,
  children
}: {
  value: I18nContextValue;
  children?: ReactNode;
}) {
  return createElement(I18nContext.Provider, { value }, children);
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}
